#Listas são estruturas de dados mutáveis, ou seja, podem ser alteradas após a criação. Elas são usadas para armazenar uma coleção de itens, que podem ser de diferentes tipos de dados.

# Listas criadas com colchetes []
frutas = ["laranja", "maca", "uva"]
print(frutas)

frutas = []
print(frutas)

# Listas criadas com a função list()
letras = list("python")
print(letras)

numeros = list(range(10))
print(numeros)

# Listas podem conter diferentes tipos de dados
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)

# Indice de uma lista começa em 0, ou seja, o primeiro elemento da lista tem índice 0, o segundo elemento tem índice 1, e assim por diante. Para acessar um elemento específico da lista, basta usar o nome da lista seguido do índice entre colchetes [].

frutas = ["maçã", "laranja", "uva", "pera"]

print(frutas[0])  # maçã
print(frutas[2])  # uva
print(frutas[1])  # laranja
print(frutas[3])  # pera
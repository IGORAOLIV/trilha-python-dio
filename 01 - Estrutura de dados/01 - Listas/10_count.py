# O método count() é utilizado para contar o número de ocorrências de um elemento específico em uma lista. Ele recebe um único argumento, que é o elemento a ser contado, e retorna o número de vezes que esse elemento aparece na lista.

cores = ["vermelho", "azul", "verde", "azul"]

print(cores.count("vermelho"))  # 1
print(cores.count("azul"))  # 2
print(cores.count("verde"))  # 1

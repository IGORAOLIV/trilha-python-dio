# A união de conjuntos é uma operação que combina os elementos de dois ou mais conjuntos, resultando em um novo conjunto que contém todos os elementos distintos dos conjuntos originais. Em Python, podemos usar o método union() ou o operador | para realizar a união de conjuntos.

conjunto_a = {1, 2}
conjunto_b = {3, 4}

resultado = conjunto_a.union(conjunto_b)
print(resultado)

# A interseção de conjuntos é uma operação que retorna um novo conjunto contendo apenas os elementos que estão presentes em ambos os conjuntos originais. Em Python, podemos usar o método intersection() ou o operador & para realizar a interseção de conjuntos.

conjunto_a = {1, 2, 3}
conjunto_b = {2, 3, 4}

resultado = conjunto_a.intersection(conjunto_b)
print(resultado)
